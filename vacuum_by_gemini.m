function vacuum_monolith_simulation()
    % 1. Инициализация параметров среды в нормированных единицах (l_p = 1, c = 1)
    N = 40;                 % Размер расчетного куба вакуума (N x N x N ячеек)
    [X, Y, Z] = meshgrid(linspace(-15, 15, N));
    
    dt = 0.2;               % Шаг по времени (условие Куранта: dt < dx/c)
    timesteps = 100;        % Количество кадров анимации
    
    % Поля смещения точек монолита u = [Ux, Uy, Uz]
    Ux = zeros(N, N, N);    Uy = zeros(N, N, N);    Uz = zeros(N, N, N);
    % Поля скоростей (первые производные по времени)
    Vx = zeros(N, N, N);    Vy = zeros(N, N, N);    Vz = zeros(N, N, N);
    
    % 2. Задание начального возмущения: ЧИСТЫЙ ВИХРЕВОЙ СКРУТ (Заряд-Вихрь)
    % u_init = curl(Потенциал), что гарантирует div(u) = 0 (несжимаемость)
    R2 = X.^2 + Y.^2 + Z.^2;
    sigma = 3.0;            % Радиус локализации первичного возмущения
    factor = exp(-R2 / (2 * sigma^2));
    
    % Задаем кручение вокруг оси Z
    Ux = -Y .* factor;
    Uy =  X .* factor;
    Uz =  zeros(N, N, N); % Чистый поперечный сдвиг
    
    % Подготовка окна визуализации
    figure('Color', 'k', 'Position', [100, 100, 800, 700]);
    
    % 3. Основной цикл динамики упругого монолита (FDTD метод)
    for t = 1:timesteps
        % Вычисление Лапласиана (оператор упругости Ньютона-Коши для сдвига)
        % В MATLAB del2(U, dx) возвращает 1/6 * nabla^2 (в 3D)
        L_Ux = 6 * del2(Ux);
        L_Uy = 6 * del2(Uy);
        L_Uz = 6 * del2(Uz);
        
        % Уравнение движения монолита: d2u/dt2 = c^2 * nabla^2(u)
        % Обновляем скорости точек вакуума (учитывая локальную связность)
        Vx = Vx + L_Ux * dt;
        Vy = Vy + L_Uy * dt;
        Vz = Vz + L_Uz * dt;
        
        % Обновляем смещения
        Ux = Ux + Vx * dt;
        Uy = Uy + Vy * dt;
        Uz = Uz + Vz * dt;
        
        % 4. Визуализация центрального сечения монолита (плоскость Z = 0)
        clf;
        mid = round(N/2);
        
        % Считаем модуль деформации среды (напряжение)
        A = sqrt(Ux.^2 + Uy.^2 + Uz.^2);
        
        % Отрисовка деформационного поля через слайс
        slice(X, Y, Z, A, [], [], 0);
        shading interp;
        colormap(hot);
        colorbar;
        caxis([0 2]);
        
        hold on;
        % Накладываем векторные стрелки скоростей в плоскости среза
        quiver3(X(:,:,mid), Y(:,:,mid), Z(:,:,mid), ...
                Vx(:,:,mid), Vy(:,:,mid), Vz(:,:,mid), 1.5, ...
                'Color', [0 0.8 1], 'LineWidth', 1);
        
        title(['Динамика вакуумного монолита. Шаг времени: ' num2col(t)], 'Color', 'w');
        axis equal;
        axis([-12 12 -12 12 -5 5]);
        view(0, 90); % Взгляд сверху на плоскость вращения
        set(gca, 'Color', 'k', 'XColor', 'w', 'YColor', 'w', 'ZColor', 'w');
        drawnow;
    end
end