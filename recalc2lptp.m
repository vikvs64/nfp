%% Комплексный скрипт сквозного расчета физических констант
% Цепочка преобразований: СИ -> LT-система -> Планковские единицы
% Логика: G полностью поглощается геометрией LT, переход к Планку идет напрямую.
% Модификация: Заряд имеет кинематическую размерность l^2/t (циркуляция вихря).
%
% =========================================================================
% МАТЕМАТИЧЕСКИЕ ФОРМУЛЫ ПЕРЕХОДА (ДЛЯ МОНОГРАФИИ):
% 1. СИ -> LT:
%    k0 = 4*pi*G               ->  M_lt = k0 * m_si      [l^3/t^2]
%    k1 = sqrt(4*pi*G/eps0)/c  ->  Q_lt = k1 * q_si      [l^2/t]
%    hbar_lt = k0 * hbar_si                              [l^5/t^3]
%
% 2. Масштабы Планковской сетки:
%    l_p = sqrt(hbar_lt / (4*pi*c^3)) = sqrt(G*hbar/c^3)
%    t_p = sqrt(hbar_lt / (4*pi*c^5)) = sqrt(G*hbar/c^5)
%
% 3. Вакуумные пределы деформации в LT:
%    Limit_lt   = sqrt(4*pi * hbar_lt * c)               [l^3/t^2]
%    Limit_q_lt = sqrt(4*pi * hbar_lt / c)               [l^2/t]
%
% 4. Замыкание на сетку Планка (Безразмерные инварианты):
%    c_p       = c * t_p / l_p          == 1
%    hbar_p    = hbar_lt * t_p^3 / l_p^5 == 4*pi
%    Limit_m_p = Limit_lt * t_p^2 / l_p^3 == 4*pi
%    Limit_q_p = Limit_q_lt * t_p / l_p^2 == 4*pi
% =========================================================================

clear; clc;
format long g;

% Флаг выбора числовых данных:
% true  - точные исторические значения из вашего скрипта Maple 5
% false - современные высокоточные константы (CODATA)
USE_HISTORICAL = false;

if USE_HISTORICAL
    fprintf('Режим: Использование исторических констант из Maple...\n\n');
    pi_val = pi;                   
    c      = 2.997925e8;           % Скорость света [м/с]
    qe     = 1.60217733e-19;       % Заряд электрона [Кл]
    me     = 9.1091e-31;           % Масса электрона [кг]
    mp     = 1.67252e-27;          % Масса протона [кг]
    mn     = 1.67482e-27;          % Масса нейтрона [кг]
    h      = 6.6260755e-34;        % Постоянная Планка [Дж*с]
    G      = 6.67259e-11;          % Гравитационная постоянная [м^3/(кг*с^2)]
else
    fprintf('Режим: Использование современных констант CODATA...\n\n');
    pi_val = pi;
    c      = 299792458;            
    qe     = 1.602176634e-19;      
    me     = 9.1093837015e-31;
    mp     = 1.67262192369e-27;
    mn     = 1.67492749804e-27;
    h      = 6.62607015e-34;       
    G      = 6.67430e-11;
end

%% =========================================================================
%% ЧАСТЬ 1. Электродинамика и нормировочные коэффициенты в СИ
%% =========================================================================
eps0  = 10^7 / (4 * c^2 * pi_val);   % Электрическая постоянная
mu0   = 4 * pi_val * 10^(-7);         % Магнитная постоянная
alpha = qe^2 / (2 * eps0 * h * c);    % Постоянная тонкой структуры
hbar  = h / (2 * pi_val);             % Редуцированная постоянная Планка

% Коэффициенты перехода из СИ в геометрическую LT-систему
k0 = 4 * pi_val * G;                  % Масштаб массы (килограммы -> м^3/с^2)
k1 = sqrt(4 * pi_val * G / eps0) / c; % Масштаб заряда (кулоны -> м^2/с, новый базис)

%% =========================================================================
%% ЧАСТЬ 2. Расчет параметров в автономной LT-системе (Базис Континуума)
%% =========================================================================
Qe_lt   = k1 * qe;                    % Кинематический заряд электрона [м^2/с]
Me_lt   = k0 * me;                    % Кинематическая масса электрона [м^3/с^2]
Mp_lt   = k0 * mp;                    % Кинематическая масса протона [м^3/с^2]
hbar_lt = k0 * hbar;                  % Нормированный квант действия [м^5/с^3]

% Геометрические масштабы вихревой структуры электрона
r_e   = Qe_lt^2 / (4 * pi_val * Me_lt);       % Классический радиус электрона [м]
r__0  = r_e / 2;                              % Радиус волнового ядра r_0 [м]
omega = c / r__0;                             % Циклическая частота пульсации вакуума
nu0   = omega / (2 * pi_val);                 % Частота основного возмущения [Гц]

% Силовые и динамические характеристики среды
a_0    = c^2 / r__0;                          % Предельное ускорение среды [м/с^2]
g_0    = Me_lt / (4 * pi_val * r__0^2);       % Геометрическая гравитация на радиусе r_0
PHI_0  = Qe_lt / (4 * pi_val * r__0);         % Кинематический потенциал
E_0    = Qe_lt / (4 * pi_val * r__0^2);       % Напряженность деформации

%% =========================================================================
%% ЧАСТЬ 3. Прямой переход из LT в Планковские единицы
%% =========================================================================
l_p = sqrt(hbar_lt / (4 * pi_val * c^3));     % Планковская длина [м]
t_p = sqrt(hbar_lt / (4 * pi_val * c^5));     % Планковское время [с]

% Раздельные фундаментальные пределы деформации монолита для массы и заряда
Limit_lt   = sqrt(4 * pi_val * hbar_lt * c);  % Механический предел массы [м^3/с^2]
Limit_q_lt = sqrt(4 * pi_val * hbar_lt / c);  % Электродинамический предел заряда [м^2/с]

% --- Перевод констант и пределов в чистый масштаб планковской сетки ---
c_p       = c * t_p / l_p;                    % Скорость света (инвариант = 1)
hbar_p    = hbar_lt * (t_p^3) / (l_p^5);      % Квант действия (инвариант = 4*pi)
Limit_m_p = Limit_lt * (t_p^2) / (l_p^3);     % Единичный предел массы (инвариант = 4*pi)
Limit_q_p = Limit_q_lt * t_p / (l_p^2);       % Единичный предел заряда / циркуляции (инвариант = 4*pi)

% Перевод характеристик элементарного вихря в безразмерный планковский вид
m_e_p = Me_lt / Limit_lt;       % Масса электрона (относительно предела)
m_p_p = Mp_lt / Limit_lt;       % Масса протона (относительно предела)
q_e_p = Qe_lt / Limit_q_lt;     % Заряд электрона (относительно предела)
r0_p  = r__0 / l_p;             % Радиус волнового ядра
nu0_p = nu0 * t_p;              % Частота пульсаций ядра

%% =========================================================================
%% ЧАСТЬ 4. Вывод и верификация результатов
%% =========================================================================
fprintf('======================= ПАРАМЕТРЫ СИ И КОЭФФИЦИЕНТЫ =======================\n');
fprintf('eps0     = %.12e\n', eps0);
fprintf('alpha    = %.8f (1/alpha = %.6f)\n', alpha, 1/alpha);
fprintf('k0       = %.12e\n', k0);
fprintf('k1       = %.12e\n', k1);

fprintf('\n================== ГЕОМЕТРИЧЕСКИЙ LT-ПОРТРЕТ ВИХРЯ ==================\n');
fprintf('hbar_lt  = %.12e м^5/с^3\n', hbar_lt);
fprintf('Qe_lt    = %.12e м^2/с\n', Qe_lt);
fprintf('Me_lt    = %.12e м^3/с^2\n', Me_lt);
fprintf('r_e      = %.12e м\n', r_e);
fprintf('r_0      = %.12e м\n', r__0);
fprintf('nu_0     = %.12e Гц\n', nu0);

fprintf('\n============== ПРЯМОЙ ПЕРЕХОД LT M/C -> ПЛАНК lp/tp ==============\n');
fprintf('l_p      = %.12e м\n', l_p);
fprintf('t_p      = %.12e с\n', t_p);
fprintf('-------------------- ФУНДАМЕНТАЛЬНЫЕ КОНСТАНТЫ СЕТКИ ----------------------\n');
fprintf('c        = %.12e lp/tp\n', c_p);
fprintf('hbar     = %.12e lp^5/tp^3  (строго 4*pi)\n', hbar_p);
fprintf('Limit_m  = %.12e lp^3/tp^2  (строго 4*pi)\n', Limit_m_p);
fprintf('Limit_q  = %.12e lp^2/tp    (строго 4*pi)\n', Limit_q_p);
fprintf('--------------------- ПАРАМЕТРЫ ЧАСТИЦ (ВИХРЕЙ) ---------------------------\n');
fprintf('m_e      = %.12e lp^3/tp^2\n', m_e_p);
fprintf('m_p      = %.12e lp^3/tp^2\n', m_p_p);
fprintf('q_e      = %.12e lp^2/tp    (строго sqrt(alpha))\n', q_e_p);
fprintf('r_0      = %.12e lp\n', r0_p);
fprintf('nu_0     = %.12e 1/tp\n', nu0_p);
fprintf('===========================================================================\n');